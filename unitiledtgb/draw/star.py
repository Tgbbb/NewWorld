import turtle

turtle.screensize(bg='black')

turtle.begin_fill()
turtle.fillcolor('white')
turtle.pencolor('white')
turtle.forward(200)
turtle.right(144)
turtle.forward(200)
turtle.right(144)
turtle.forward(200)
turtle.right(144)
turtle.forward(200)
turtle.right(144)
turtle.forward(200)
turtle.end_fill()
turtle.up()

turtle.goto(-240,200)
turtle.color('white')
turtle.write('作者：唐铬彬',font='隶书 -28 bold')
turtle.up()

turtle.done()
